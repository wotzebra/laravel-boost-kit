# Project Coding Guidelines

- This codebase follows Wotzebra's internal code style.
- Always activate the `wotz-laravel-php-standards` skill when writing, editing, reviewing, or formatting any Laravel or PHP code.
- If this is an **API project** (e.g. `routes/api.php` exists, controllers return JSON), also activate the `wotz-laravel-php-api` skill.
- If this is a **web project** (e.g. Blade views or Livewire components are present), also activate the `wotz-laravel-php-web` skill.